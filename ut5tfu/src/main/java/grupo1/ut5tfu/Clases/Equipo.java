package grupo1.ut5tfu.Clases;

public class Equipo {
    private String nombre;
    private String pais;
    private int entrenador_ci;

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNombre() {
        return nombre;
    }

    public void setPais(String pais) {
        this.pais = pais;
    }

    public String getPais() {
        return pais;
    }

    public int getEntrenador_ci() {
        return entrenador_ci;
    }

    public void setEntrenador_ci(int entrenador_ci) {
        this.entrenador_ci = entrenador_ci;
    }
}
